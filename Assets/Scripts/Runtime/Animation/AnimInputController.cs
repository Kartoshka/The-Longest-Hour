using UnityEngine;
using System;
using System.Collections;

/// <summary>
/// 
/// </summary>
public class AnimInputController : MonoBehaviour
{
	//////////////////////////////////////////////////////////////////////////////////////////
	#region GameObjects
	//////////////////////////////////////////////////////////////////////////////////////////

	public Rigidbody m_rigidBody;
	public Animator m_animator;
	public string m_runSpeedParam;
	public string m_attackParam;

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region  Constructors
	//////////////////////////////////////////////////////////////////////////////////////////

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Interface Methods
	//////////////////////////////////////////////////////////////////////////////////////////

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Accessors
	//////////////////////////////////////////////////////////////////////////////////////////  

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Methods
	//////////////////////////////////////////////////////////////////////////////////////////

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Runtime
	//////////////////////////////////////////////////////////////////////////////////////////

	// Update is called once per frame
	void Update()
	{
		if(m_animator != null)
		{
			ProcessInputs();
		}
	}

	private void ProcessInputs()
	{
		float normalizedRunSpeed = Math.Abs(Input.GetAxis("Horizontal")) + Math.Abs(Input.GetAxis("Vertical"));
		//float normalizedRunSpeed = velocity.normalized.x;
		m_animator.SetFloat(m_runSpeedParam, normalizedRunSpeed);
		if(Input.GetButtonDown("Fire1"))
		{
			m_animator.SetBool(m_attackParam, true);
		}
    }

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Persistence
	//////////////////////////////////////////////////////////////////////////////////////////

	#endregion
}