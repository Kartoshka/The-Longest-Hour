using UnityEngine;
using System;
using MOJ.Helpers;

public class GameObjectTransformProvider : TransformProvider
{
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Atrributes
	//////////////////////////////////////////////////////////////////////////////////////////

	private GameObject m_gameObject = null;

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Accessors
	//////////////////////////////////////////////////////////////////////////////////////////

	public GameObject getGameObject() { return m_gameObject; }
	public void setGameObject(GameObject gameObject) { m_gameObject = gameObject; }

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Interface
	//////////////////////////////////////////////////////////////////////////////////////////

	public override Transform getTransform()
	{
		if(m_gameObject != null)
		{
			return m_gameObject.transform;
        }
		return null;
	}

	#endregion
}
