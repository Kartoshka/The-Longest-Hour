using UnityEngine;
using System;
using System.Collections;

using MOJ.Helpers;

[Serializable]
public class LinearInputMoverBehaviorData
{
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Datatypes
	////////////////////////////////////////////////////////////////////////////////////////// 

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Properties
	////////////////////////////////////////////////////////////////////////////////////////// 

	// Add new variables for loading and saving here.
	public Vector3 stepSize = Vector3.zero;
	public bool enableUserInput = true;

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region  Constructors
	////////////////////////////////////////////////////////////////////////////////////////// 

	public LinearInputMoverBehaviorData() { }

	public LinearInputMoverBehaviorData(LinearInputMoverBehaviorData data)
	{
		if (data != null)
		{
			copy(data);
		}
	}

	#endregion
	//////////////////////////////////////////////////////////////////////////////////////////
	#region Methods
	//////////////////////////////////////////////////////////////////////////////////////////

	public void copy(LinearInputMoverBehaviorData rhs)
	{
		stepSize = rhs.stepSize;
		enableUserInput = rhs.enableUserInput;
    }

	#endregion
}